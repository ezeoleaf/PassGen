function translate()
{
    const lang = getItem('lang');
    let vS = document.getElementsByTagName('span');
    for(let i = 0; i < vS.length; i++)
    {
        if(dictionary[vS[i].innerText.trim().toLowerCase()] != undefined)
        {
            vS[i].innerText = (lang == 'es') ? dictionary[vS[i].innerText.trim().toLowerCase()].es : dictionary[vS[i].innerText.trim().toLowerCase()].en;
        }
    }
    let vL = document.getElementsByTagName('label');
    for(let i = 0; i < vL.length; i++)
    {
        if(dictionary[vL[i].innerText.trim().toLowerCase()] != undefined)
        {
            vL[i].innerText = (lang == 'es') ? dictionary[vL[i].innerText.trim().toLowerCase()].es : dictionary[vL[i].innerText.trim().toLowerCase()].en;
        }
    }
}

function getTranslatedWord(word)
{
    const lang = getItem('lang');
    let wordToReturn = word;
    if(dictionary[word.trim().toLowerCase()] != undefined)
    {
        wordToReturn = (lang == 'es') ? dictionary[word.trim().toLowerCase()].es : dictionary[word.trim().toLowerCase()].en;
    }
    return wordToReturn;
}

